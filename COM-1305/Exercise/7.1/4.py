def Add(number1, number2) -> None:
    sum = int(number1) + int(number2)
    print("ผลลัพธ์ของการบวกเลข เท่ากับ ", sum)


x = input("ป้อนตัวเลขที่ 1: ")
y = input("ป้อนตัวเลขที่ 2: ")

Add(x, y)

# NOTE: Input
# ป้อนตัวเลขที่ 1: 1
# ป้อนตัวเลขที่ 2: 1

# NOTE: Output
# ผลลัพธ์ของการบวกเลข เท่ากับ  2
