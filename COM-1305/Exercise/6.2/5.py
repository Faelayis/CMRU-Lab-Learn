# จำนวนตัวเลขในเซตที่มีค่ำมำกที่สุด คือ 50
# จำนวนตัวเลขในเซตที่มีค่ำน้อยที่สุด คือ 10
# ผลรวมข้อมูลทั้งหมดในเซต คือ 150
# ชนิดของตัวแปร x คือ <class 'set'>
